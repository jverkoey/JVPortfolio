<h1>Adopting Three20</h1>

<p>My second internship at Facebook, simply put, <em>rocked</em>. From September to December of 2010
  I was given the opportunity to work on a number of kickass projects on the Facebook mobile team.
</p>

<div class="right_annotation">The new Facebook offices &rarr;</div>

<p>One such project was Three20.</p>

<p>Due to
  <a href="http://blog.jeffverkoeyen.com/strips-postmortem">my experience with Strips</a> on my
  previous academic term, I had a fairly solid grasp of the framework that powered Facebook's
  iPhone app. But anyone who has used Three20 knows one thing: it was (and still is) in dire need
  of documentation.
</p>

<h2>Some background</h2>

<p>Joe Hewitt, the developer behind tools such as Firebug, developed Three20 specifically for
  the Facebook iPhone app. He then open sourced it on GitHub, and since then it has skyrocketed
  to the top 10 charts for
  <a href="http://github.com/popular/forked">popular forked</a> and
  <a href="http://github.com/popular/watched">popular watched</a>, with 321 forks and
  2290 followers at the time of this writing.
</p>

<p>Simply put, this framework is watched by a <em>lot</em> of iPhone developers.</p>

<h2>Joe's departure</h2>

<p>Midway through my internship, Joe Hewitt posted
  <a href="http://joehewitt.com/post/on-middle-men/">his farewell to the iPhone community</a>.</p>

<p>It caused quite the stir.</p>

<p>For me, the biggest question after his departure was what would happen to Three20. In November
  the framework still didn't have a consistent source for information, so I built
  <a href="http://Three20.info">Three20.info</a>.
</p>

<h2>Three20.info</h2>

<p>The site is composed of three primary sets of information: auto-generated doxygen
  documentation, a blog, and detailed, article-like documentation of the Three20 features. The
  site currently reaches over 5000 developers per month and is completely open source, making it
  possible for <em>anyone</em> to contribute.
</p>

<h2>Cleaning the framework</h2>

<p>My work on Three20 since adopting it has involved a large amount of dirty work. Documentation,
  stylistic cleanup, and tearing apart a spiderweb of dependencies have been the highest
  priorities. In the near future, I will also make available a complete rewrite of some of
  the library's internals for handling tables, as well as providing the first iteration of support
  for the iPad.
</p>


